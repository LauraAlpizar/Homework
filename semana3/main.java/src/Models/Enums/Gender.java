package Models.Enums;

// Definimos el enum 'Gender' que representa el género de un personaje.
public enum Gender {
    Male,       // El personaje es masculino.
    Female,     // El personaje es femenino.
    Genderless, // El personaje no tiene género.
    unknown     // El género del personaje es desconocido (probablemente por errores en la API o no se especifica).
}
